			<p>Graduates of London College of Garden Design and Greenwich University respectively, Katerina and Daniela’s shared vision and passion is to create sustainable gardens that also meet the complex wishes and needs of their clients. The pair are also committed to educating young people in all aspects of gardening and horticulture, and they’re currently in the process of establishing an exciting charity program to this effect.</p>
			 
			<p>Attention to detail lies at the heart of all Katerina and Daniela do, and the service you’ll receive with Purpleberry Gardens includes not only preliminary planning and concept visits, but also after-care checks and careful maintenance scheduling. Daniela and Katerina only work with recognised and reputable contractors, those who they trust to carry out works with absolute professionalism and attention to detail.</p>
