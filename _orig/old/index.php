		</div>
		<div class="content">
			<p>GLUTTONY, one of the deadly sins we so very often and very willingly forget about. Our garden, named <strong>E123</strong> after one of the worst food additives, is a way to represent and describe this sin in an abstract, artistic and playful manner. In this garden we're highlighting the great amount of food being consumed and wasted every day in the UK and other western worlds through the over-sized sculptures, not mistakenly designed to remind people of the iconic food brands of our time, which should not only attract the eye, but also point out the enormous and unnecessary levels of food consumption whilst millions of people, including children starve.
